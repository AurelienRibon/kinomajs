/********************************************************************/

static void noise_generator(Ipp32s *dst,
                            int    len,
                            int    *noiseState,
                            Ipp32s *norm,
                            int    *scale)
{
  Ipp64s norml, ltemp;
  Ipp32s normi;
  int seed, i;
  int shift;

  norml = 0;
  seed = *noiseState;

  if (len == 1) shift = 0;
  else {
    int tmp = len - 1;
    shift = -1;
    while (tmp > 0) {
      shift++;
      tmp >>= 1;
    }
  }
  for (i = 0; i < len; i++) {
    seed = (1664525L * seed) + 1013904223L;
    dst[i] = (Ipp32s)seed;
    norml += ((Ipp64s)seed * seed) >> shift;
  }
  *noiseState = seed;
  normi = (Ipp32s)(norml >> 32);

  while (normi < 1073741824) {
    shift--;
    normi <<= 1;
  }

  ltemp  = ((Ipp64s)normi * normi ) >> 32; /* Q30 */
  ltemp  = ltemp * 13563;                  /* Q44 */
  ltemp -= (Ipp64s)normi * 16760;          /* Q44 */
  ltemp += 39107824713728LL;               /* Q44 */
  normi  = (Ipp32s)(ltemp >> 14);          /* Q30 */
  shift += (30 * 2 + 31 + 32);
  if (shift & 1) {
    normi = (Ipp32s)(((Ipp64s)normi * 1518500250) >> 31);
    shift--;
  }

  *scale = shift >> 1;
  *norm = normi;
}

