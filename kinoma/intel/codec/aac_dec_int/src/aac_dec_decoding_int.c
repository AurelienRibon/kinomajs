void coupling_gain_calculation(sCoupling_channel_element *pElement,
                               sCoupling_channel_data *pData,
                               int cc_gain[18][MAX_GROUP_NUMBER][MAX_SFB],
                               int cc_gain_factor[18][MAX_GROUP_NUMBER][MAX_SFB])
{
  int *pgain_table = gain_table[pData->gain_element_scale];
  int shift = (3 - pData->gain_element_scale);
  int ch = 0;
  int c, g, sfb;

  for (c = 0; c < pData->num_gain_element_lists; c++) {
    if (pData->cge[c]) {
      int fac = -pElement->cc_fact[c][0][0];
      int ifac = fac >> shift;
      int scale = pgain_table[fac - (ifac << shift)];

      for (g = 0; g < pElement->stream.num_window_groups; g++) {
        for (sfb = 0; sfb < pElement->stream.max_sfb; sfb++) {
          if (pElement->stream.sfb_cb[g][sfb] != ZERO_HCB) {
            cc_gain[ch][g][sfb] = scale;
            cc_gain_factor[ch][g][sfb] = ifac - 30;
          } else {
            cc_gain[ch][g][sfb] = 0;
            cc_gain_factor[ch][g][sfb] = 0;
          }
        }
      }
    } else {
      if (pData->gain_element_sign) {
        for (g = 0; g < pElement->stream.num_window_groups; g++) {
          for (sfb = 0; sfb < pElement->stream.max_sfb; sfb++) {
            if (pElement->stream.sfb_cb[g][sfb] != ZERO_HCB) {
              int sign = pElement->cc_fact[c][g][sfb] & 1;
              int fac = -(pElement->cc_fact[c][g][sfb] >> 1);
              int ifac = fac >> shift;
              int scale = pgain_table[fac - (ifac << shift)];

              if (sign) scale = -scale;
              cc_gain[ch][g][sfb] = scale;
              cc_gain_factor[ch][g][sfb] = ifac - 30;
            } else {
              cc_gain[ch][g][sfb] = 0;
              cc_gain_factor[ch][g][sfb] = 0;
            }
          }
        }
      } else {
        for (g = 0; g < pElement->stream.num_window_groups; g++) {
          for (sfb = 0; sfb < pElement->stream.max_sfb; sfb++) {
            if (pElement->stream.sfb_cb[g][sfb] != ZERO_HCB) {
              int fac = -(pElement->cc_fact[c][g][sfb]);
              int ifac = fac >> shift;
              int scale = pgain_table[fac - (ifac << shift)];
              cc_gain[ch][g][sfb] = scale;
              cc_gain_factor[ch][g][sfb] = ifac - 30;
            } else {
              cc_gain[ch][g][sfb] = 0;
              cc_gain_factor[ch][g][sfb] = 0;
            }
          }
        }
      }
    }

    if (pData->cc_lr[c] == 3) {
      ch++;
    } else {
      ch += 2;
    }
  }
}

/********************************************************************/

void coupling_spectrum(AACDec *state,
                       sCoupling_channel_data *pData,
                       int *c_spectrum,
                       int curr_win_sequence,
                       int cc_gain[18][MAX_GROUP_NUMBER][MAX_SFB],
                       int cc_gain_factor[18][MAX_GROUP_NUMBER][MAX_SFB])
{
  int *m_spectrum, *pc_spectrum;
  int c, ch, g, m_ch, sfb, w;
  int *sfb_offset = pData->sfb_offset;
  int max_sfb = pData->max_sfb;
  int num_window_groups = pData->num_window_groups;
  int num_el = 0;
  int tmp[1024];
  int target_win_sequence, scalef;

  ch = 0;

  for (c = 0; c < pData->num_coupled_elements + 1; c++) {
    m_ch = pData->cc_target_ch[c];
    if (m_ch < 0) continue;

    /* if cpe and only right channel */
    if ((pData->cc_target_id[c]) && (pData->cc_lr[num_el] == 1))
      m_ch++;

    target_win_sequence = state->com.m_curr_win_sequence[m_ch];
    scalef = 0;
    if ((target_win_sequence == EIGHT_SHORT_SEQUENCE) &&
        (curr_win_sequence != EIGHT_SHORT_SEQUENCE)) {
      scalef = -3;
    } else if ((target_win_sequence != EIGHT_SHORT_SEQUENCE) &&
               (curr_win_sequence == EIGHT_SHORT_SEQUENCE)) {
      scalef = 3;
    }

    m_spectrum = state->m_spectrum_data[m_ch];
    pc_spectrum = c_spectrum;
    for (g = 0; g < num_window_groups; g++) {
      int len_window_group = pData->len_window_group[g];
      for (w = 0; w < len_window_group; w++) {
        for (sfb = 0; sfb < max_sfb; sfb++) {
          int len = sfb_offset[sfb + 1] - sfb_offset[sfb];
          ippsMulC_32s_Sfs_x(pc_spectrum + sfb_offset[sfb], cc_gain[ch][g][sfb],
                           tmp, len, scalef - cc_gain_factor[ch][g][sfb]);
          ippsAdd_32s_ISfs_x(tmp, m_spectrum + sfb_offset[sfb], len, 0);
        }
        m_spectrum += 128;
        pc_spectrum += 128;
      }
    }

    if (pData->cc_target_id[c]) { /* if cpe */
      if (pData->cc_lr[num_el] == 3) {

        target_win_sequence = state->com.m_curr_win_sequence[m_ch+1];
        scalef = 0;
        if ((target_win_sequence == EIGHT_SHORT_SEQUENCE) &&
            (curr_win_sequence != EIGHT_SHORT_SEQUENCE)) {
          scalef = -3;
        } else if ((target_win_sequence != EIGHT_SHORT_SEQUENCE) &&
                  (curr_win_sequence == EIGHT_SHORT_SEQUENCE)) {
          scalef = 3;
        }

        m_spectrum = state->m_spectrum_data[m_ch+1];
        pc_spectrum = c_spectrum;
        for (g = 0; g < num_window_groups; g++) {
          int len_window_group = pData->len_window_group[g];
          for (w = 0; w < len_window_group; w++) {
            for (sfb = 0; sfb < max_sfb; sfb++) {
              int len = sfb_offset[sfb + 1] - sfb_offset[sfb];
              ippsMulC_32s_Sfs_x(pc_spectrum + sfb_offset[sfb], cc_gain[ch+1][g][sfb],
                               tmp, len, scalef - cc_gain_factor[ch+1][g][sfb]);
              ippsAdd_32s_ISfs_x(tmp, m_spectrum + sfb_offset[sfb], len, 0);
            }
            m_spectrum += 128;
            pc_spectrum += 128;
          }
        }
        num_el++;
      } else if (pData->cc_lr[num_el] == 0) {

        target_win_sequence = state->com.m_curr_win_sequence[m_ch+1];
        scalef = 0;
        if ((target_win_sequence == EIGHT_SHORT_SEQUENCE) &&
            (curr_win_sequence != EIGHT_SHORT_SEQUENCE)) {
          scalef = -3;
        } else if ((target_win_sequence != EIGHT_SHORT_SEQUENCE) &&
                  (curr_win_sequence == EIGHT_SHORT_SEQUENCE)) {
          scalef = 3;
        }

        m_spectrum = state->m_spectrum_data[m_ch+1];
        pc_spectrum = c_spectrum;
        for (g = 0; g < num_window_groups; g++) {
          int len_window_group = pData->len_window_group[g];
          for (w = 0; w < len_window_group; w++) {
            for (sfb = 0; sfb < max_sfb; sfb++) {
              int len = sfb_offset[sfb + 1] - sfb_offset[sfb];
              ippsMulC_32s_Sfs_x(pc_spectrum + sfb_offset[sfb], cc_gain[ch][g][sfb],
                               tmp, len, scalef - cc_gain_factor[ch][g][sfb]);
              ippsAdd_32s_ISfs_x(tmp, m_spectrum + sfb_offset[sfb], len, 0);
            }
            m_spectrum += 128;
            pc_spectrum += 128;
          }
        }
      }
    }
    ch += 2;
    num_el++;
  }
}

/********************************************************************/

void coupling_samples(AACDec *state,
                      sCoupling_channel_data *pData,
                      int *c_samlpes,
                      int cc_gain[18][MAX_GROUP_NUMBER][MAX_SFB],
                      int cc_gain_factor[18][MAX_GROUP_NUMBER][MAX_SFB])
{
  int c, ch, m_ch;
  int num_el = 0;
  int tmp[1024];

  ch = 0;

  for (c = 0; c < pData->num_coupled_elements + 1; c++) {
    m_ch = pData->cc_target_ch[c];
    if (m_ch < 0) continue;

    /* if cpe and only right channel */
    if ((pData->cc_target_id[c]) && (pData->cc_lr[num_el] == 1))
      m_ch++;

    ippsMulC_32s_Sfs_x(c_samlpes, cc_gain[ch][0][0],
                     tmp, 1024, -cc_gain_factor[ch][0][0]);
    ippsAdd_32s_ISfs_x(tmp, state->m_curr_samples[m_ch], 1024, 0);

    if (pData->cc_target_id[c]) { /* if cpe */
      if (pData->cc_lr[num_el] == 3) {
        ippsMulC_32s_Sfs_x(c_samlpes, cc_gain[ch+1][0][0],
                         tmp, 1024, -cc_gain_factor[ch+1][0][0]);
        ippsAdd_32s_ISfs_x(tmp, state->m_curr_samples[m_ch+1], 1024, 0);
        num_el++;
      } else if (pData->cc_lr[num_el] == 0) {
        ippsMulC_32s_Sfs_x(c_samlpes, cc_gain[ch][0][0],
                         tmp, 1024, -cc_gain_factor[ch][0][0]);
        ippsAdd_32s_ISfs_x(tmp, state->m_curr_samples[m_ch+1], 1024, 0);
      }
    }
    ch += 2;
    num_el++;
  }
}

