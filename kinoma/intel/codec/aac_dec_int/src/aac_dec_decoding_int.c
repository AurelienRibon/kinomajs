/********************************************************************/

static IppStatus Pow43_16s32s(const Ipp16s *pSrc,
                                    Ipp32s *pDstmant,
                                    int len)
{
  IppStatus err = ippStsNoErr;

  int     i, s, as, t, t2;

  for (i = 0; i < len; i++) {
    s = pSrc[i];
    as = abs(s);
    t = Pow43_table[as];
    t2 = t & (s >> 31);
    pDstmant[i] = t - t2 - t2;
  }
  return err;
}

/********************************************************************/

static int pow2table[] = {1073741824, 1276901417, 1518500250, 1805811301};

static void ownippsCalcSF_16s32s(const Ipp16s *pSrc,
                                 int offset,
                                 Ipp32s *mant,
                                 Ipp32s *exp,
                                 int len)
{
  int i;
  for (i = 0; i < len; i++) {
    Ipp32s itmp = (Ipp32s)pSrc[i] - offset;
    exp[i] = itmp >> 2;
    mant[i] = pow2table[itmp & 3];
  }
}

/********************************************************************/

static void ownippsScale_32s_I(Ipp32s *pSrcDst,
                               Ipp32s *mant,
                               Ipp32s *exp,
                               const Ipp32s *pBandOffset,
                               int bandsNumber,
                               int len_window_group,
                               int scalef)
{
  int sfb, bin;

  for (sfb = 0; sfb < bandsNumber; sfb++) {
    Ipp64s mtmp = (Ipp64s)mant[sfb];
    Ipp32s shift;
    Ipp64s tmp;

    shift = 43 + scalef - exp[sfb];
    if (shift > 63) shift = 63;

    for (bin = pBandOffset[sfb]*len_window_group;
         bin < pBandOffset[sfb + 1]*len_window_group; bin++) {
      tmp = (pSrcDst[bin] * mtmp) >> shift;
      pSrcDst[bin] = (int)tmp;
    }
  }
}

/********************************************************************/

int ics_apply_scale_factors(s_SE_Individual_channel_stream *pData,
                            Ipp32s *p_spectrum)
{
  int     g;
  int     w_num;
  Ipp32s  mant_sf[60];
  Ipp32s  exp_sf[60];

  if (pData->window_sequence != EIGHT_SHORT_SEQUENCE) {
    if (pData->sfb_offset_long_window[pData->max_sfb] > 0) {
      Pow43_16s32s(pData->spectrum_data, p_spectrum,
        pData->sfb_offset_long_window[pData->max_sfb]);

      ownippsCalcSF_16s32s(pData->sf[0], SF_OFFSET, mant_sf, exp_sf, pData->max_sfb);
      ownippsScale_32s_I(p_spectrum, mant_sf, exp_sf,
                         pData->sfb_offset_long_window, pData->max_sfb, 1, -2);
    }

    ippsZero_32s_x(p_spectrum + pData->sfb_offset_long_window[pData->max_sfb],
                 1024 - pData->sfb_offset_long_window[pData->max_sfb]);

  } else {
    if (pData->sfb_offset_long_window[pData->max_sfb] > 0) {

      Pow43_16s32s(pData->spectrum_data, p_spectrum,
        pData->sfb_offset_short_window[pData->max_sfb] * 8);

      for (g = 0; g < pData->num_window_groups; g++) {
        w_num = pData->len_window_group[g];

        ownippsCalcSF_16s32s(pData->sf[g], SF_OFFSET, mant_sf, exp_sf, pData->max_sfb);
        ownippsScale_32s_I(p_spectrum, mant_sf, exp_sf, pData->sfb_offset_short_window,
                           pData->max_sfb, w_num, -5);

        p_spectrum +=
          w_num * pData->sfb_offset_short_window[pData->max_sfb];
      }
    }
  }

  return 0;
}

