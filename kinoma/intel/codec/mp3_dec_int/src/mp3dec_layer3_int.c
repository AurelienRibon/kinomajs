static int mp3dec_CalcSF_table[4] = {
  1073741824,
  1276901416,
  1518500249,
  1805811301,
};

static IppStatus CalcSF_16s32s(const Ipp16s *pSrc, int offset, Ipp32s *pDst_e, Ipp32s *pDst_m, int len)
{
  int     i;

  for(i = 0; i < len; i++) {
      int val = pSrc[i] - offset;

      pDst_e[i] = (val >> 2) + 2;
      pDst_m[i] = mp3dec_CalcSF_table[val & 3];
  }

  return ippStsNoErr;
}

static IppStatus Scale_32s_I(Ipp32s *pSrcDst, Ipp32s *pSF_e, Ipp32s *pSF_m, const Ipp32s *pBandOffset,
        int bandsNumber)
{
  int     sfb, bin;

  for (sfb = 0; sfb < bandsNumber; sfb++) {
    if (pSF_e[sfb] <= -45) {
      ippsZero_32s_x(pSrcDst + pBandOffset[sfb], pBandOffset[sfb + 1] - pBandOffset[sfb]);
    } else {
      for (bin = pBandOffset[sfb]; bin < pBandOffset[sfb + 1]; bin++) {
        pSrcDst[bin] = ((Ipp64s) pSrcDst[bin] * pSF_m[sfb]) >> (19 - pSF_e[sfb]);
      }
    }
  }

  return ippStsNoErr;
}


/******************************************************************************
//  Name:
//    Dequant
//
//  Description:
//    Decoding of coded subband samples in order to recover the original
//    quantized values
//
//  Input Arguments:
//    DC - point to sDecoderContext structure
//    gr - number of granule
//    ch - number of channel
//
//  Output Arguments:
//    smpl_xr[ch] - array of recovered samples
//
//  Returns:
//    1 - all ok
//
******************************************************************************/

static int mp3dec_Dequant(MP3Dec *state, int gr, int ch)
{
    int xx_e, xx_m;
    short   scl;
    int     sfb;
    int     nz;

    IppMP3FrameHeader *header = &(state->com.header);

    int     *sfb_long = (int *)mp3dec_sfBandIndex[header->id + state->com.mpg25][header->samplingFreq].l;

    int   (*GlobalScaleFactor_e)[2] = state->GlobalScaleFactor_e;
    int   (*GlobalScaleFactor_m)[2] = state->GlobalScaleFactor_m;
    sScaleFactors *ScaleFactors = state->com.ScaleFactors;
    unsigned int (*si_blockType)[2] = state->com.si_blockType;
    unsigned int (*si_mixedBlock)[2] = state->com.si_mixedBlock;
    short   (*si_pSubBlkGain)[2][3] = state->com.si_pSubBlkGain;
    unsigned int (*si_preFlag)[2] = state->com.si_preFlag;
    unsigned int (*si_sfScale)[2] = state->com.si_sfScale;
    sampleint *smpl_xr = state->smpl_xr;       /* out of dequantizer */
    sampleshort *smpl_xs = state->com.smpl_xs;       // out of huffman

    scl = -2 - 2 * (short)si_sfScale[gr][ch];
    xx_e = GlobalScaleFactor_e[gr][ch];
    xx_m = GlobalScaleFactor_m[gr][ch];
    Pow43_16s32s((*smpl_xs)[ch], (*smpl_xr)[ch], 576);

    if (si_blockType[gr][ch] != 2) {
        int   tmp22_e[22], tmp22_m[22];
        short   sf_tmp[21];

        if (si_preFlag[gr][ch] == 0) {
            for (sfb = 0; sfb < 21; sfb++)
                sf_tmp[sfb] = scl * ScaleFactors[ch].l[sfb];
        } else {
            for (sfb = 0; sfb < 21; sfb++)
                sf_tmp[sfb] = scl * (ScaleFactors[ch].l[sfb] + pretab[sfb]);
        }
        CalcSF_16s32s(sf_tmp, 0, tmp22_e, tmp22_m, 21);
        tmp22_e[21] = 2;
        tmp22_m[21] = 1 << 30;

        ippsMulC_32s_ISfs_x(xx_m, tmp22_m, 22, 32);
        ippsAddC_32s_ISfs_x(xx_e, tmp22_e, 22, 0);
        Scale_32s_I((*smpl_xr)[ch], tmp22_e, tmp22_m, sfb_long, 22);
    } else {
        int   tmpSF_e[3], tmpSF_m[3], tmp13_e[3][13], tmp13_m[3][13];
        short   sf_tmp[13];
        int     n, j, wnd;
        int     sfbStart, sfbEnd;
        int     *sfb_short = (int *)mp3dec_sfBandIndex[header->id + state->com.mpg25][header->samplingFreq].s;

        sfbEnd = 0; sfbStart = 0;
        if (si_mixedBlock[gr][ch]) {
            if (header->id == 1) {
                sfbEnd = 8;
            } else {
                sfbEnd = 6;
            }
            sfbStart = 3;

            for (sfb = 0; sfb < sfbEnd; sfb++)
                sf_tmp[sfb] = scl * ScaleFactors[ch].l[sfb];

            CalcSF_16s32s(sf_tmp, 0, tmp13_e[0], tmp13_m[0], sfbEnd);
            ippsMulC_32s_ISfs_x(xx_m, tmp13_m[0], sfbEnd, 32);
            ippsAddC_32s_ISfs_x(xx_e, tmp13_e[0], sfbEnd, 0);
            Scale_32s_I((*smpl_xr)[ch], tmp13_e[0], tmp13_m[0], sfb_long, sfbEnd);
        }

        CalcSF_16s32s(si_pSubBlkGain[gr][ch], 0, tmpSF_e, tmpSF_m, 3);

        for (wnd = 0; wnd < 3; wnd++) {
                int x_m, x_e;
            for (sfb = sfbStart; sfb <= 12; sfb++)
                sf_tmp[sfb] = scl * ScaleFactors[ch].s[wnd][sfb];

            CalcSF_16s32s(sf_tmp, 0, tmp13_e[wnd], tmp13_m[wnd], 13);
            x_m = MUL32_MP3_32S(xx_m, tmpSF_m[wnd]);
            x_e = xx_e + tmpSF_e[wnd];
            ippsMulC_32s_ISfs_x(x_m, tmp13_m[wnd], 13, 32);
            ippsAddC_32s_ISfs_x(x_e, tmp13_e[wnd], 13, 0);
        }

        j = sfb_long[sfbEnd];
        for (sfb = sfbStart; sfb <= 12; sfb++) {
            n = sfb_short[sfb + 1] - sfb_short[sfb];
            for (wnd = 0; wnd < 3; wnd++) {
                Scale_32s_I(&((*smpl_xr)[ch][j]) - sfb_short[sfb], &tmp13_e[wnd][sfb], &tmp13_m[wnd][sfb], sfb_short + sfb, 1);
                j += n;
            }
        } // for
    }

    nz = state->com.non_zero[ch];
    while(nz > 0 && ((*smpl_xr)[ch])[nz - 1] == 0)
        nz --;
    state->com.non_zero[ch] = nz;

    return 1;
}

