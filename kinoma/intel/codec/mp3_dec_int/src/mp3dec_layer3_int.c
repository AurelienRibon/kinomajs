static IppStatus Pow43_16s32s(const Ipp16s *pSrc, Ipp32s *pDst, int len)
{
  IppStatus err = ippStsNoErr;

  int     i, s, t, t2;

  for (i = 0; i < len; i++) {
    s = pSrc[i];
    t = Pow43_table[abs(s)];
    t2 = t & (s >> 31);
    pDst[i] = t - t2 - t2;
  }
  return err;
}

